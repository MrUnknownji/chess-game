export const findKing = (
  board: Board,
  color: PieceColor,
): [number, number] | null => {
  for (let row = 0; row < 8; row++) {
    for (let col = 0; col < 8; col++) {
      const piece = board[row][col];
      if (piece && piece.type === "king" && piece.color === color) {
        return [row, col];
      }
    }
  }
  return null;
};
